<h2 id="Introduction">Introduction</h2>
<p>
    The RoL d20 set of role-playing rules is based on the
    <a href="http://en.wikipedia.org/wiki/D20_system">d20 system</a> and
    <a href="http://en.wikipedia.org/wiki/Dungeons_%26_Dragons">3.5E D&amp;D</a>,
    with some additional inspiration from other d20 systems as well as
    <a href="http://en.wikipedia.org/wiki/Dungeons_%26_Dragons">4E D&amp;D</a> and
    <a href="http://en.wikipedia.org/wiki/Dungeons_%26_Dragons">5E D&amp;D</a>.
    The RoL rules have been designed to be simpler and more flexible, but also
    more realistic, than the original d20 rules.
    However, the set of classes, character races, monsters, spells, and so forth
    are basically the same, and the RoL rules try to remain
    faithful to the heroic and high-fantasy feel of traditional D&amp;D.
</p>

<h3 id="Motivation">Motivation</h3>
<p>There are a whole lot of reasons behind the design of these rather
    extensive &quot;house rules&quot;. First, let me say that I really like the
    regular d20 and D&amp;D rules; they are clear, consistent, and fairly
    simple compared to most of the older RPG systems I have had the
    privilege of playing. Still, I felt that the rules could be made even
    simpler and more generic, and I also felt that many of the rules could
    be made more realistic without making them more complicated or less
    entertaining. In total, there are quite a few d20 rules that I'm not
    satisfied with, many of which are actually a legacy from previous
    versions of D&amp;D. Here are some examples:</p>
<ul>
    <li>The concepts of spell slots and spell memorization never really
        appealed to me. What is the rationale in having a wizard be able to cast a
        level 5 spell but not a level 1 spell, just because he has already
        used up all of his level 1 spell slots for the day and &quot;lost&quot; them
        from memory? I greatly prefer a system that uses some sort of power
        points. I also wanted to avoid the long preparation times where spell
        casters have to decide which spells to memorize for the day, while the
        other players just sit around and wait.</li>
    <li>I wanted a system where skills play a greater part for all
        characters, not just for rogues and bards. By changing most of the
        feats and class abilities into skills, I feel that I get a more
        flexible and realistic system. Furthermore, I got rid of some really
        uninteresting but nearly mandatory feats, such as Weapon Focus, Spell
        Focus, and Combat Casting, by turning them into benefits granted
        automatically by skills.</li>
    <li>Also, turning feats and class abilities into skills makes it
        possible for characters to choose how much or how little to specialize
        in each ability. Even prestige classes have been replaced with skills,
        so becoming a Shadowdancer, an Arcane Trickster, or similar type of
        specialist no longer requires switching to a new class.</li>
    <li>Another benefit is that the stacking of abilities between different
        classes becomes more logical when that ability is based on a skill
        shared between the classes. Even spell casters can now multi-class
        without necessarily sacrificing their ability to cast higher level
        spells (as long as the two classes share some of their supernatural
        skills).</li>
    <li>I've wondered why characters should get better only at attacking
        when increasing their level, while very few classes get better at
        defending themselves. So, I've changed the old Base Attack Bonus into
        a much more generic system that can be used for defense as well as
        offense. Also, increasing one's weapon skill will provide bonuses to
        parrying as well as attacking.</li>
    <li>I never liked the concept of armor making a character harder to hit
        rather than soaking up some of the impact, so I have changed the armor
        system to essentially provide the wearer with Damage Reduction instead.</li>
    <li>I wanted to reduce the relatively easy access to resurrection
        magic. I prefer the concept of fate points or hero points that is used
        in many other systems to avoid a high frequency of character death.</li>
</ul>

<h3 id="MainFeatures">Key Changes and Features</h3>
<p>This is a summary of the most important changes in these rules
    compared to traditional 3.5E D&amp;D:</p>
<ul>
    <li>The core rules have been made more streamlined, consistent, and
        generic, with regards to <a href="hb02_coremech.php#Actions">d20
            checks</a>, <a href="hb02_coremech.php#Modifiers">modifiers</a>, and
        <a href="hb02_coremech.php#Descriptors">descriptors</a>.</li>
    <li>The rules are based on a more distinct definition of <a
            href="hb02_coremech.php#Characteristics">core characteristics</a> and
        their <a href="hb02_coremech.php#CharDependencies">dependencies</a>.</li>
    <li>The old saving throws have been replaced with passive <a
            href="hb02_coremech.php#DefenseScores">defense scores</a> (very
        similar to D&amp;D 4E).</li>
    <li>A set of <a href="hb02_coremech.php#SocialScores">social scores</a>
        (social class, wealth class, influence, and reputation) has been
        added.</li>
    <li>The old level-based ability bonuses have been replaced with a more
        generic <a href="hb02_coremech.php#ImprPts">improvement point</a>
        system.</li>
    <li><a href="hb02_coremech.php#FatePts">Fate points</a> have been
        introduced to reduce the importance of resurrection magic.</li>
    <li>Open-ended <a href="hb02_coremech.php#Actions">d20 checks</a> are
        used for all skill checks, attack rolls, etc.</li>
    <li>Most class traits, feats, and even monster abilities have been changed into
        <a href="hb09_skills.php#Skills">skills</a>.</li>
    <li>Racial traits have been separated into racial and cultural traits.</li>
    <li>Some races have been changed into templates.</li>
    <li>The Barbarian class has been changed into a set of skills available to the
        Fighter class.</li>
    <li>The Paladin and Blackguard classes have been changed into a more generic
        Templar class.</li>
    <li>Skills have been divided into
        <a href="hb09_skills.php#SkillBenefits">skill benefits</a>
        (bonuses provided by the skill) and
        <a href="hb09_skills.php#SkillActions">skill actions</a>
        (things you can do with the skill).</li>
    <li>A system based on <a href="hb02_coremech.php#ActionPts">action
            points</a> (increasing with level) has replaced all the old action
        types.</li>
    <li>The size of a weapon affects attack speed. Larger weapons are slower but
        can provide attacks of opportunity against opponents with smaller weapons.</li>
    <li>Both natural armor and manufactured armor now provide damage
        resistance (instead of making the target harder to hit).</li>
    <li>Weapons and shields provide a parry bonus based on weapon and
        skill.</li>
    <li>Low-level magic items have been replaced with additional levels of
        high quality equipment.</li>
    <li>Spells and most supernatural abilities are now powered either by
        stamina points (physical stamina) or power points (mental stamina).</li>
    <li>Spells have variable parameters and variations instead of a fixed
        effect and spell level.</li>
    <li>Spells make use of opposed checks and opposed power levels much
        more than before.</li>
    <li>The creation of magic items has been made more streamlined.</li>
    <li><a href="hb05_magic.php#Residuum">Residuum</a> (from D&amp;D 4E)
        has been adopted as an alternative to spending XP when creating magic
        items.</li>
    <li>Most costs, prices, treasures, etc. have been changed one
        magnitude, from gold pieces to silver.</li>
</ul>

