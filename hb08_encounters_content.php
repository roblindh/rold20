<h3 id="Reactions">Reactions</h3>
<p>
    Every creature has a limited number of reactions per round that can be used to perform actions outside of its own turn.
    Each reaction can be used for any action that has an action time of &quot;react&quot; and has had its triggering condition satisfied.
    Note, however, that each creature can only use one reaction per triggering condition.
</p>
<p>
    <dfn>Maximum number of reactions (per round) = (Total AP / 10) (rounded down) + bonus reactions</dfn>
</p>
<p>
    The reaction occurs immediately when triggered, interrupts the normal initiative sequence, and is usually resolved before any triggering action.
    This potentially means that a reaction can prevent triggering actions from being completed.
    For example, if an opponent's movement triggers an attack of opportunity, a successful trip attack can prevent that movement.
    If an attack of opportunity triggered by spellcasting causes damage, the damage caused will penalize the caster's spellcasting check.
    And if a spell is successfully counterspelled, its casting can be interrupted or altered.
</p>
<p>
    Unless otherwise specified, the reactive action requires only the reaction itself and costs no extra AP to perform,
    but many reactions do allow the use of saved AP to boost the action and provide bonuses.
</p>
<p>
    Note that it is possible for a reaction to trigger its own reactions (from other creatures).
</p>

<h3 id="Experience">Experience</h3>

<h4 id="ExperienceAwards">Awarding Experience</h4>
<p>
    If you know the EL of an encounter, then calculating the experience award is quite easy:
</p>
<ol>
    <li>Total XP = EL × 300.</li>
    <li>Divide the total XP by the number of participants (on the winning side). Don’t count familiars, mounts, animal companions, and pets. Calculate only half a share for hirelings, henchmen, cohorts, etc.</li>
    <li>Adjust the XP share for each participant based on relative level.</li>
    <ul>
        <li>For a character 3 to 5 levels above EL, halve XP.</li>
        <li>For a character 6 or more levels above EL, award no XP.</li>
        <li>For a character 3 or more levels below EL, award double XP.</li>
    </ul>
    <li>Adjust the XP share for each participant based on the degree of participation. A participant who falls unconscious or flees early in a combat should receive less XP than one who stays until the end.</li>
</ol>
<p>
    The DM is also free to provide additional XP for some non-encounter situations,
    such as major goals accomplished, exceptional role-playing, brilliant ideas, etc.
</p>

<div class="optionalrule">
    <p>
        <em id="FasterAdvancement">Faster Advancement</em> (optional rule):
        Multiply all XP rewards and treasures by 1.5 (or some other factor above 1).
    </p>
</div>
<div class="optionalrule">
    <p>
        <em id="SlowerAdvancement">Slower Advancement</em> (optional rule):
        Multiply all XP rewards and treasures by 0.5 (or some other factor below 1).
    </p>
</div>

<h4>Encounter Levels</h4>
<p>
    Encounter level (or EL) is a measure of how difficult a certain encounter is and is used to calculate the amount of XP awarded.
    For a good but not too risky challenge, the EL should equal the average party level in a party with four characters.
    For fewer than four characters, a slightly lower EL is appropriate, and vice versa.
</p>

